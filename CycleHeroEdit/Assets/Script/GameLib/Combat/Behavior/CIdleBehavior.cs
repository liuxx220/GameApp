	}
	
	//-----------------------------------------------------------------------------------------
	// 退出本行为
	//-----------------------------------------------------------------------------------------
	public override void Exit()
	{
		
	}
	
	//-----------------------------------------------------------------------------------------
	// 退出本行为
	//-----------------------------------------------------------------------------------------
	public override void Update( )
	{


	}

	//-----------------------------------------------------------------------------------------
	// 处理动作底层发来的事件
	//-----------------------------------------------------------------------------------------
	public override void doAnimEvent(  string strParam )
	{
		
	}
}




